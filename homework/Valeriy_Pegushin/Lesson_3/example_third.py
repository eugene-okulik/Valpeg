number_c = 4
number_d = 16

# Определяем среднее арифметическое, как сумма всех чисел, деленное на их количество
result_1 = (number_c + number_d) / 2

# Определяем среднее геометрическое, как умножение всех чисел и извлечение из них корня по количеству чисел
result_2 = (number_c * number_d) / 8

print(result_1)
print(result_2)
