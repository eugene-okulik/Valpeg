number_g = 4
number_h = 16

# Находим длину гипотенузы прямоугольного треугольника, как сумма квадратов длин катетов
result_1 = (number_g * 4) + (number_h * 16)

# Находим площадь прямоугольного треугольника, как произведение длин его катетов, разделенному на 2
result_2 = (number_g * number_h) / 2

print(result_1)
print(result_2)
