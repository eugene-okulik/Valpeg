# Декоратор repeat_me, который будет повторять вызов декорируемой функции
def repeat_me(func):
    # Внутренняя функция-обертка, которая принимает любые аргументы
    def wrapper(*args, **kwargs):
        # Извлекаем значение 'count' из kwargs, если его нет - используем 1 по умолчанию
        # При этом удаляем 'count' из kwargs, чтобы не передавать его дальше
        count = kwargs.pop('count', 1)

        # Выполняем декорируемую функцию count раз
        for _ in range(count):
            # Вызываем оригинальную функцию с оставшимися аргументами
            func(*args, **kwargs)

    # Возвращаем функцию-обертку
    return wrapper


# Применяем декоратор repeat_me к функции example
@repeat_me
def example(text):
    # Тело оригинальной функции - печатает переданный текст
    print(text)


# Вызываем декорированную функцию
# Будет выполнено 2 раза (count=2), так как декоратор повторяет вызов
example('print me', count=2)
